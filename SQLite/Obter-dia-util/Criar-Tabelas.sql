-- Gerar dias uteis
DROP TABLE IF EXISTS DIAS_UTEIS;
CREATE TABLE DIAS_UTEIS AS
SELECT 
    data,
    CASE
        WHEN dia_semana IN (5, 6) THEN 'Fim de semana'
        WHEN data IN (SELECT data_feriado FROM feriados) THEN 'Feriado'
        ELSE 'Dia Util'
    END AS descricao
FROM (
  WITH RECURSIVE dates(data) AS (
    VALUES('2019-01-01')
    UNION ALL
    SELECT date(data, '+1 day')
    FROM dates
    WHERE data < '2100-12-25'
  )
  SELECT data, (CAST(strftime('%w', data) AS INT) + 6) % 7 dia_semana
  FROM dates order by data asc
